from fractions import Fraction

# 1. One-half
fraction1 = Fraction(1, 2)

# 2. Three
fraction2 = Fraction(3)

# 3. Two-thirds from a string
fraction3 = Fraction("2/3")

# 4. 0.6 as a fraction
fraction4 = Fraction("0.6")

# 5. Seven-eighths
fraction5 = Fraction(7, 8)

# Print all five to verify
print(f"1: {fraction1}, 2: {fraction2}, 3: {fraction3}, 4: {fraction4}, 5: {fraction5}")